    fun currencyConversor(
        from: String, fromQuantity: Double, to: String, exchangeRates: Map<String, Double>
    ): Double {
        val fromRate =
            exchangeRates[from] ?: throw IllegalArgumentException("Divisa de origen no encontrada")
        val toRate =
            exchangeRates[to] ?: throw IllegalArgumentException("Divisa de destino no encontrada")
        return fromQuantity * (toRate / fromRate)
    }
